// Get Panchangam data for a specific date
export function getPanchangamData(year, month, day) {
    const monthData = PANCHANGAM_2026[month];

    if (!monthData || !monthData[day]) {
        return {
            tamil_date: "---",
            nakshatra: "---",
            tithi: "---",
            events: [],
            festival: null,
            dayType: DAY_TYPES.REGULAR
        };
    }

    return monthData[day];
}

// Get Tamil date for a specific Gregorian date (from JSON data)
export function getTamilDate(year, month, day) {
    const data = getPanchangamData(year, month, day);
    if (data.tamil_date === "---") {
        return { day: day, month: "---", monthEn: "---" };
    }

    const parts = data.tamil_date.split(" ");
    const tamilMonth = parts[0];
    const tamilDay = parseInt(parts[1]) || day;

    const monthMap = {
        "சித்திரை": "Chithirai",
        "வைகாசி": "Vaikasi",
        "ஆனி": "Aani",
        "ஆடி": "Aadi",
        "ஆவணி": "Aavani",
        "புரட்டாசி": "Purattasi",
        "ஐப்பசி": "Aippasi",
        "கார்த்திகை": "Karthigai",
        "மார்கழி": "Margazhi",
        "தை": "Thai",
        "மாசி": "Maasi",
        "பங்குனி": "Panguni"
    };

    return {
        day: tamilDay,
        month: tamilMonth,
        monthEn: monthMap[tamilMonth] || tamilMonth
    };
}

// Get Tamil months that span a Gregorian month
export function getTamilMonthsInGregorianMonth(year, month) {
    const daysInMonth = new Date(year, month + 1, 0).getDate();
    const uniqueMonths = [];

    for (let day = 1; day <= daysInMonth; day++) {
        const tamilDate = getTamilDate(year, month, day);
        if (tamilDate.month !== "---" && !uniqueMonths.find(m => m.name === tamilDate.month)) {
            uniqueMonths.push({ name: tamilDate.month, nameEn: tamilDate.monthEn });
        }
    }

    return uniqueMonths;
}
